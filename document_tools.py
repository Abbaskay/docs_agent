def generate_invoice(
    business_name: str,
    business_email: str,
    client_name: str,
    client_email: str,
    items: str,
    due_date: str = "",
    payment_method: str = "Bank Transfer",
    notes: str = "",
) -> str:
    """Generate a professional invoice with automatic totals.

    Args:
        business_name:   Name of the issuing business.
        business_email:  Business contact email.
        client_name:     Client or customer name.
        client_email:    Client email address.
        items:           Line items in format 'Name | qty | unit_price' per line.
        due_date:        Payment due date (default: 30 days from today).
        payment_method:  Accepted payment method.
        notes:           Additional notes for the client.

    Returns:
        Formatted invoice with computed totals as a string.
    """
    global _last_generated

    if not due_date:
        due_date = (datetime.now() + timedelta(days=30)).strftime("%d %B %Y")
    if not notes:
        notes = "Thank you for your business. Payment within due date is appreciated."

    invoice_number = f"INV-{datetime.now().strftime('%Y%m')}-{uuid.uuid4().hex[:4].upper()}"
    date_str = datetime.now().strftime("%B %d, %Y")
    divider = "═" * 62
    thin = "─" * 62

    # Parse items
    parsed_items = []
    subtotal = 0.0
    placeholder_used = False

    for line in items.strip().splitlines():
        line = line.strip()
        if not line:
            continue
        parts = [p.strip() for p in line.split("|")]
        try:
            if len(parts) >= 3:
                name = parts[0]
                qty = float(parts[1])
                unit_price = float(parts[2])
                line_total = qty * unit_price
                subtotal += line_total
                parsed_items.append((name, qty, unit_price, line_total))
            else:
                raise ValueError
        except (ValueError, IndexError):
            placeholder_used = True
            parsed_items.append(("[Item Description]", 1, 0.0, 0.0))

    if not parsed_items or placeholder_used:
        parsed_items = [
            ("Website Design", 1, 50000.0, 50000.0),
            ("SEO Setup", 1, 15000.0, 15000.0),
        ]
        subtotal = 65000.0

    tax = subtotal * 0.18
    total = subtotal + tax

    # Build items table
    header = f"{'DESCRIPTION':<32} {'QTY':>5} {'UNIT PRICE':>12} {'TOTAL':>10}"
    item_lines = []
    for name, qty, up, lt in parsed_items:
        item_lines.append(f"{name:<32} {qty:>5.0f} {'₹'+f'{up:,.2f}':>12} {'₹'+f'{lt:,.2f}':>10}")
    items_block = "\n".join(item_lines)

    output = f"""{divider}
INVOICE
{divider}
From: {business_name}
      {business_email}

To:   {client_name}
      {client_email}

Invoice No: {invoice_number}
Date:       {date_str}
Due Date:   {due_date}
{thin}

ITEMS
{thin[:5]}
{header}
{thin}
{items_block}
{thin}
{'Subtotal:':>50} {'₹'+f'{subtotal:,.2f}':>10}
{'GST (18%):':>50} {'₹'+f'{tax:,.2f}':>10}
{divider}
{'TOTAL DUE:':>50} {'₹'+f'{total:,.2f}':>10}
{divider}

Payment Method: {payment_method}

NOTES
{thin[:5]}
{notes}

{thin}
Generated by Hyperzod Document Agent
{date_str}
All amounts in INR (₹). Prices include applicable taxes."""

    _last_generated["type"] = "invoice"
    _last_generated["content"] = output
    return output


