def generate_cv(
    full_name: str,
    email: str,
    phone: str,
    location: str,
    job_title: str,
    summary: str = "",
    experience: str = "",
    education: str = "",
    skills: str = "",
    languages: str = "",
) -> str:
    """Generate a professional CV / Resume.

    Creates a fully formatted CV with all standard sections. Optional fields
    are pre-filled with intelligent placeholder text if not provided.

    Args:
        full_name:  Candidate's full name.
        email:      Candidate's email address.
        phone:      Candidate's phone number.
        location:   Candidate's city / country.
        job_title:  Target job title / current role.
        summary:    Professional summary paragraph.
        experience: Work experience block (multi-line).
        education:  Education block (multi-line).
        skills:     Comma-separated skills list.
        languages:  Languages and proficiency levels.

    Returns:
        Formatted CV as a string.
    """
    global _last_generated

    if not summary:
        summary = (
            f"Results-driven {job_title} with X+ years of experience in [industry]. "
            "Passionate about [key area]. Seeking to [goal]."
        )
    if not experience:
        experience = (
            f"[Company Name] — [City] | [Start] – [End]\n"
            f"  {job_title}\n"
            "  • [Key achievement or responsibility]\n"
            "  • [Key achievement or responsibility]\n"
            "  • [Key achievement or responsibility]"
        )
    if not education:
        education = (
            "[Degree Name] in [Field of Study]\n"
            "  [University Name] — [City] | [Year]"
        )
    if not skills:
        skills = "[Skill 1], [Skill 2], [Skill 3], [Skill 4]"
    if not languages:
        languages = "English (Fluent)"

    date_str = datetime.now().strftime("%B %d, %Y")
    divider = "═" * 52
    thin = "─" * 52

    output = f"""{divider}
                   {full_name.upper()}
{divider}
{job_title}
📧 {email}  |  📞 {phone}  |  📍 {location}
{thin}

PROFESSIONAL SUMMARY
{thin[:20]}
{summary}

WORK EXPERIENCE
{thin[:15]}
{experience}

EDUCATION
{thin[:9]}
{education}

SKILLS
{thin[:6]}
{skills}

LANGUAGES
{thin[:9]}
{languages}

{divider}
Generated by Hyperzod Document Agent
{date_str}
{divider}"""

    _last_generated["type"] = "cv"
    _last_generated["content"] = output
    return output


