def export_as_markdown(
    content: str = "",
    document_title: str = "",
) -> str:
    """Export content or the last generated document as clean markdown.

    Args:
        content:        Text to export. Uses last generated doc if empty.
        document_title: Title for the markdown document. Auto-detected if empty.

    Returns:
        Full export output including BEGIN/END markers as a string.
    """
    global _last_generated

    if not content:
        content = _last_generated.get("content", "")
    if not content:
        return "No content to export."

    if not document_title:
        type_map = {
            "cv": "Curriculum Vitae",
            "cover_letter": "Cover Letter",
            "proposal": "Business Proposal",
            "report": "Project Report",
            "invoice": "Invoice",
            "email": "Email Template",
            "edited": "Improved Document",
        }
        document_title = type_map.get(_last_generated.get("type", ""), "Document")

    date_str = datetime.now().strftime("%B %d, %Y")

    # Convert plain-text formatting to markdown
    lines = content.splitlines()
    md_lines = []
    for line in lines:
        stripped = line.strip()
        # Divider lines
        if re.match(r"^[═─]{4,}$", stripped):
            md_lines.append("---")
        # ALL CAPS headings (min 3 chars, no digits)
        elif (
            stripped.isupper()
            and len(stripped) >= 3
            and not stripped.startswith("INV-")
            and not stripped.startswith("RPT-")
            and not stripped.startswith("PROP-")
        ):
            md_lines.append(f"## {stripped.title()}")
        # Bullet points
        elif stripped.startswith("• "):
            md_lines.append("- " + stripped[2:])
        # Numbered lists already fine
        elif re.match(r"^\d+\.", stripped):
            md_lines.append(stripped)
        else:
            md_lines.append(line)

    md_body = "\n".join(md_lines)

    # Header and footer
    header = (
        f"# {document_title}\n"
        f"> Generated by Hyperzod Document Agent on {date_str}\n\n---\n"
    )
    footer = (
        "\n---\n"
        f"*Document generated by Hyperzod Document Agent*  \n"
        f"*Export date: {date_str}*"
    )

    full_md = header + md_body + footer
    word_count = len(full_md.split())
    thin = "─" * 49

    output = (
        f"✅ MARKDOWN EXPORT READY\n"
        f"{thin}\n"
        f"Copy everything between the markers below:\n\n"
        f"═══ BEGIN MARKDOWN ═══\n\n"
        f"{full_md}\n\n"
        f"═══ END MARKDOWN ═══\n\n"
        f"{thin}\n"
        f"{word_count} words | Ready to paste into Notion, GitHub, or any markdown editor."
    )

    return output

