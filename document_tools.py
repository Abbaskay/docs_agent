def edit_and_improve(
    content: str = "",
    improvement_type: str = "general",
    instruction: str = "",
) -> str:
    """Edit and improve provided content or the last generated document.

    Args:
        content:          Text to improve. Uses last generated doc if empty.
        improvement_type: Mode: general, formal, concise, professional, grammar.
        instruction:      Optional specific instruction to apply.

    Returns:
        Improved content with word count comparison as a string.
    """
    global _last_generated

    if not content:
        content = _last_generated.get("content", "")
    if not content:
        return (
            "No content to improve. "
            "Please provide content or generate a document first."
        )

    original_word_count = len(content.split())
    improved_content = content

    if improvement_type == "formal":
        replacements = {
            "I think": "I believe",
            "i think": "I believe",
            " get ": " obtain ",
            " use ": " utilize ",
            " show ": " demonstrate ",
            " help ": " assist ",
            " need ": " require ",
            " talk ": " discuss ",
            "find out": "ascertain",
            "Find out": "Ascertain",
        }
        for old, new in replacements.items():
            improved_content = improved_content.replace(old, new)

    elif improvement_type == "concise":
        verbose_map = {
            "in order to": "to",
            "due to the fact that": "because",
            "at this point in time": "now",
            "in the event that": "if",
            "for the purpose of": "for",
            "with regard to": "regarding",
            "in spite of the fact that": "although",
        }
        notes = []
        for verbose, concise in verbose_map.items():
            if verbose in improved_content.lower():
                notes.append(f'  • "{verbose}" → "{concise}"')
                improved_content = re.sub(
                    re.escape(verbose), concise, improved_content, flags=re.IGNORECASE
                )
        if notes:
            improved_content += (
                "\n\n[CONCISE NOTE: The following replacements were applied:]\n"
                + "\n".join(notes)
            )

    elif improvement_type == "professional":
        improved_content = (
            "[DOCUMENT PURPOSE: Professional communication document]\n\n"
            + improved_content
            + "\n\n[REVISION TRACKING: v1.0 — Initial draft]"
        )

    elif improvement_type == "grammar":
        lines = improved_content.split(". ")
        fixed = []
        for line in lines:
            line = line.strip()
            if line:
                line = line[0].upper() + line[1:]
                if not line.endswith((".", "!", "?")):
                    line += "."
            fixed.append(line)
        improved_content = " ".join(fixed)
        improved_content += (
            "\n\n[GRAMMAR NOTE: Sentences capitalized and punctuated. "
            "Please review their/there/they're and its/it's usage manually.]"
        )

    else:  # general
        improved_content = (
            "[GENERAL IMPROVEMENT APPLIED]\n\n"
            + improved_content
            + "\n\n[EDITORIAL NOTES]\n"
            "• Review all [BRACKETED] placeholders and replace with specific content.\n"
            "• Ensure consistent tense throughout the document.\n"
            "• Verify contact details and dates are accurate.\n"
            "• Proofread for flow and tone before sharing."
        )

    improved_word_count = len(improved_content.split())
    thin = "─" * 49

    instruction_line = ""
    if instruction:
        instruction_line = f"\nApplied instruction: {instruction}"

    output = (
        f"✏️ IMPROVED DOCUMENT ({improvement_type.title()} Mode)\n"
        f"{thin}\n"
        f"Original: {original_word_count} words\n"
        f"Improved: {improved_word_count} words\n"
        f"{thin}\n\n"
        f"{improved_content}\n\n"
        f"{thin}"
        f"{instruction_line}\n"
        f"[Review all [BRACKETED] notes before finalizing]"
    )

    _last_generated["type"] = _last_generated.get("type", "edited")
    _last_generated["content"] = improved_content
    return output


