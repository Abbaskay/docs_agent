def answer_from_document(question: str) -> str:
    """Answer a question using only the loaded document content.

    Performs keyword-based sentence scoring to find the most relevant
    passages. Does not use any external knowledge.

    Args:
        question: The question to answer from the document.

    Returns:
        Relevant sentences from the document as a formatted string.
    """
    global _document

    if not _document["loaded"]:
        return (
            "⚠️ No document loaded yet.\n"
            "Please paste your document content and I will load and "
            "summarize it first, then answer your questions."
        )

    stopwords = {
        "the", "a", "an", "is", "are", "was", "were", "in", "on", "at",
        "to", "for", "of", "and", "or", "but", "it", "this", "that",
        "with", "from", "by",
    }
    tokens = [
        w.lower().strip(".,;:!?")
        for w in question.split()
        if w.lower() not in stopwords
    ]

    sentences = [s.strip() for s in _document["content"].split(".") if s.strip()]

    scored = []
    for sent in sentences:
        sent_lower = sent.lower()
        score = sum(1 for t in tokens if t in sent_lower)
        if score > 0:
            scored.append((score, sent))

    scored.sort(key=lambda x: x[0], reverse=True)
    top = [s for _, s in scored[:5]]

    thin = "─" * 49
    filename = _document["filename"]

    if not top:
        return (
            f"The document does not contain information relevant to: '{question}'\n\n"
            f"Loaded document: {filename}"
        )

    answer_body = "\n\n".join(f"{s.strip()}." for s in top)

    return (
        f"🔍 ANSWER FROM DOCUMENT: {filename}\n"
        f"Question: {question}\n"
        f"{thin}\n\n"
        f"Based on the loaded document:\n\n"
        f"{answer_body}\n\n"
        f"{thin}\n"
        f"[Answer sourced from: {filename}]\n"
        f"[Only document content used — no external knowledge]"
    )


